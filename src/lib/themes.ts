export const THEMES_BY_ID: Record<string, Raw> = Object.fromEntries(
  THEMES.map((t) => [t.id, t]),
);

export const DEFAULT_THEME_ID = 'pure';

export function applyTheme(themeId: string, customVars?: Record<string, string>): void {
  const theme = THEMES_BY_ID[themeId] ?? THEMES_BY_ID[DEFAULT_THEME_ID];
  const root = document.documentElement;
  // Reset all known variables to the theme's defaults first
  for (const t of THEMES) {
    for (const k of Object.keys(t.vars)) {
      root.style.removeProperty(k);
    }
  }
  for (const [k, v] of Object.entries(theme.vars)) {
    root.style.setProperty(k, v);
  }
  if (customVars) {
    for (const [k, v] of Object.entries(customVars)) {
      root.style.setProperty(k, v);
    }
  }
  root.dataset.themeId = theme.id;
  root.dataset.themeMode = theme.mode;
  root.style.colorScheme = theme.mode;
}
